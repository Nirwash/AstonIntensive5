package com.nirwashh.astonintensive5;


public class Contact {
    String name;
    String lastname;
    String telNumber;
    int id;

    public Contact(String name, String lastname, String telNumber, int id) {
        this.name = name;
        this.lastname = lastname;
        this.telNumber = telNumber;
        this.id = id;
    }
}
